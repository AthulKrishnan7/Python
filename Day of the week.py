day_num = int(input("Enter the day number :"))
if day_num ==1 :
    print("Sunday")
elif day_num ==2 :
    print("Monday")
elif day_num ==3 :
    print("Tuesday")
elif day_num ==4 :
    print("Wednesday")
elif day_num ==5 :
    print("Thrusday")
elif day_num ==6 :
    print("Friday")
elif day_num ==7 :
    print("Saturday")
else :
    print("Not a valid input")